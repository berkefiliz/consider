<article>
    <p>
        Debates are an essential part of our society. Listing
        arguments for and against subjects in a civilized manner
        in order to convince the other party is the healthiest
        way to find a midway between two opposing opinions. This
        method works particularly well when there is no
        objective answer to the matter at hand. For instance, it
        is meaningless to argue over whether a kilogram of
        feathers is heavier than a gram of steel. Such argument
        will not have a two-sided discussion and as a
        consequence, it will not benefit any party.
    </p>
    <p>
        I love debates. I tend to debate just to be a nuisance
        sometimes, as I enjoy the train of ideas that result
        from them. I have recently discovered that
        <a href="https://www.urbandictionary.com/define.php?term=Berking" target="_blank">Urban Dictionary</a>
        defines the verb form of my name, <i>berking</i>, as
        "the act of arguing for the sake of arguing, or arguing
        to a point that is obviously false." Makes me wonder
        whether my fate was already decided over a score and two
        years ago.
    </p>
    <img src="https://talkstar-assets.s3.amazonaws.com/production/playlists/playlist_482/healthy_debate_1200x627.jpg" alt="Cool debate art" />
    <h4>
        They said images make the posts more interesting -
        <a href="https://www.ted.com/playlists/482/talks_to_support_a_healthy_deb" target="_blank">TED</a>
    </h4>
    <p>
        It is no fun to write about those regular debates
        though. I am more interested in those which have zero
        possible conclusions. I enjoy debates significantly more
        when there is no way to measure the
        <i>correctness</i> of either side of the argument. So in
        this post, I would like to explore some of those
        forever-inconclusive discussions that I have been
        carrying on my mind for a while.
    </p>
    <hr />
    <p>
        My go-to example in this topic is the idea of
        <a href="https://rationalwiki.org/wiki/Last_Thursdayism" target="_blank">Last Thursdayism</a>. It states thus:
    </p>
    <blockquote>
        Under Last Thursdayism, books, fossils, light already on
        the way from distant stars, and everything (including
        your memories of the time before last Thursday) were all
        formed at the time of creation (last Thursday) in a
        state such that they appear much older.
    </blockquote>
    <p>
        At first it is easy to deny the argument as a whole
        since you still have clear memories of Gerald's birthday
        party last weekend. You were there and you recall making
        conscious decisions such as selecting your gift, driving
        your car, knocking the door, being a victim of social
        anxiety and spending the entire two hours drinking wine
        in the least crowded corner of Gerald's mansion, and
        having to call 112 after someone drank to the point of
        alcohol poisoning. How can the stress be so fresh if
        none of these ever happened in the first place?
    </p>
    <p>
        Well, I am none the wiser. Reading the premise of Last
        Thursdayism awakens a serious doubt about my recent
        past. As much as I cannot bring concrete evidence that
        the universe was indeed created last Thursday, I also
        cannot prove otherwise. Who is to say that the so-called
        past is not an elaborate
        <a href="https://en.wikipedia.org/wiki/False_memory#Mandela_Effect">Mandela effect</a>
        which collectively fell for? Since time flows in a
        singular direction (as far as we are aware), I cannot go
        back and bring something from last Wednesday. Then we
        can declare this debate inconclusive and move on... or
        can we?
    </p>
    <img src="https://innotechtoday.com/wp-content/uploads/2018/07/MS-1-2.jpg" alt="Hey, Vsauce, Michael here" />
    <h4>
        Michael Stevens (left) -
        <a href="https://innotechtoday.com/vsauce-founder-michael-stevens/" target="_blank">Innotech Today</a>
    </h4>
    <p>
        It sounds like the argument is trying to shift the
        burden of proof. I am the one challenging the widely
        accepted view of "No, the universe goes way further back
        than last Thursday." Therefore I am the one to bring an
        evidence to the table. It is easier to explain what I
        mean with a more reasonable case.
    </p>
    <p>
        As a more practical example, one should not have to
        prove their innocence until there is concrete evidence
        against. That is fair in the correct context. As we
        cannot blame a baby for not following the law, we can
        assume they are innocent by default. If one wants to
        prove that the same person guilty later in their life,
        it is essential to point out to the exact event where
        the assumption of innocence is no longer valid. After
        all, this is a debate with a clear answer and the person
        who makes the criminal claim should have a valid reason
        for it (unless it is a very niche ethical debate that
        decides whether the person is a criminal, but that would
        be outside of the scope of this post.)
    </p>
    <p>
        I am failing to follow the same logic with Last
        Thursdayism. Yes, there are already widely accepted
        ideas on when the universe was created. One of these
        ideas is the
        <a href="https://en.wikipedia.org/wiki/Young_Earth_creationism" target="_blank">Young Earth Creationism</a>
        which is a tenet that the Earth is about 6000 years old.
        Many scientists believe that the age of the universe is
        instead
        <a href="https://en.wikipedia.org/wiki/Age_of_the_universe" target="_blank">about 13.8 billion</a>
        years. In either case, the number is an estimate rather
        than a measurement. Any proof that can be given for the
        two views does not refute Last Thursdayism. Since we
        cannot go back and check the validity of either claim,
        neither of the claims is more valid than the idea that
        the universe is a couple of days old. And that's why the
        burden of proof is shared between both parties for this
        specific case.
    </p>
    <p>
        Now that I have established my view on why certain
        arguments may spark endless and inconclusive debates, I
        would like to go do a haywire of a brainstorming
        session.
    </p>
    <hr />
    <p>
        Given the limited nature of human perception, how do we
        know some certain items do not disappear when no one
        acknowledges their existence? No, I am not talking about
        some horror scenario like
        <a href="https://en.wikipedia.org/wiki/Weeping_Angel" target="_blank">weeping angels</a>. I am talking about something more structured, more
        sentient and more conspiracy-friendly. There may be a
        whole another world we do not even know of.
    </p>
