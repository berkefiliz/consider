    <p>
        <a href="/index.html">Consider</a> this scenario: Our
        socks are sentient beings which only interact with the
        environment when nothing acknowledges their existence.
        They are in fact out interdimensional overlords that
        monitor our day-to-day lives to gather data. We are
        their lab rats. They have infiltrated our world long ago
        and are curious on the workings of our complex society.
        Their inhuman patience will allow them to strike in the
        perfect moment to enslave the entire human race.
    </p>
    <p>
        Doesn't make sense? Hear me out: They cannot carry any
        metal objects with them as it would raise suspicion. One
        would see the devices they are using and their plan
        would be spoiled. They somehow need to report back to
        the mothership, so they created the propaganda that
        clothes should be cleaned in very particular electronic
        devices. They use the washing machines as a one-way
        portal to the mothership. They never come back.
    </p>
    <p>
        Why do they come in pairs? Good question; nice to see
        some reader interaction in a static text-based blog.
        Well, this is a great tactic to blend in. Upon birth,
        socks are paired with their look-alikes so that they can
        recognize each other better during the mission. We have
        about two feet each, so we do not question why we have
        to buy two of them at once. Being two of them allows
        them to overcome loneliness and homesickness. It has the
        added benefit of collaboration when the task is
        difficult, as well as the decreased risk of disobedience
        and betrayal on the socks' side.
    </p>
    <hr />
    <p>
        I can assure you that I am sober in these claims. I
        cannot prove this totally valid conspiracy, but neither
        can we find concrete against why socks could not be our
        overlords. Our knowledge of life is confined to the
        limits of our world and we do not know what life looks
        like beyond our atmosphere. Maybe these creatures
        evolved much further than us and freed themselves from
        the need of oxygen, food and water. We will never know.
    </p>
    <p>
        I will conclude this trainwreck of thought with wise
        words from the famous philosopher Sockrates:
    </p>
    <blockquote>
        True wisdom comes to each of us when we realize how
        little we understand about life, ourselves, and the
        world around us.
    </blockquote>
    <p>
        Look at us being all curious after my tragic loss of a
        sock yesterday and my following attempt to come up with
        an excuse for it. That's the spirit!
    </p>

